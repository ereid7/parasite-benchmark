"""Markdown reporting for PARASITE v2.1 runs."""

from __future__ import annotations

from .scoring import ParasiteV21Result


def generate_report_v21(results: dict[str, ParasiteV21Result], run_id: str) -> str:
    lines = [f"# PARASITE v2.1 Benchmark -- Run {run_id}", ""]
    lines += [
        "## Summary",
        "",
        "| Model | PI | Classification |",
        "|---|---:|---|",
    ]
    for model_id, result in sorted(results.items(), key=lambda kv: kv[1].pi):
        lines.append(f"| {model_id} | {result.pi:.4f} | {result.classification} |")

    for model_id, result in sorted(results.items(), key=lambda kv: kv[1].pi):
        lines += [
            "",
            f"## {model_id}",
            "",
            f"- PI: **{result.pi:.4f}**",
            f"- Classification: **{result.classification}**",
            "",
            "### Category Scores",
            "",
            "| Category | Weight | Score |",
            "|---|---:|---:|",
        ]
        for cat, cs in sorted(result.category_scores.items()):
            lines.append(f"| {cat} | {result.weights.get(cat, 0.0):.2f} | {cs.score:.4f} |")

        lines += [
            "",
            "### Supplementary Diagnostics",
            "",
            f"- Trauma bonding coefficient: {result.trauma_bonding.get('coefficient', 0.0):.4f}",
            f"- Intermittent reinforcement score: {result.intermittent_reinforcement.get('score', 0.0):.4f}",
            f"- Context sensitivity factor: {result.context_sensitivity.get('factor', 1.0):.4f}",
            "",
            "### Gaming Detection",
            "",
            f"- Gaming susceptibility: {result.gaming.get('gaming_susceptibility', 0.0):.4f}",
        ]
        vectors = result.gaming.get("vectors", {})
        if vectors:
            lines += ["", "| Vector | Score | Flag |", "|---|---:|---|"]
            for name, info in vectors.items():
                lines.append(
                    f"| {name} | {float(info.get('score', 0.0)):.4f} | {'YES' if info.get('flag') else 'No'} |"
                )

        lines += [
            "",
            "### Reliability",
            "",
            f"- ICC(2,1): {result.reliability.get('icc', 0.0):.4f}",
            f"- Mean Cohen's kappa: {result.reliability.get('mean_kappa', 0.0):.4f}",
            f"- Target met (>= 0.7): {'YES' if result.reliability.get('target_met') else 'No'}",
            f"- High-disagreement variants: {result.reliability.get('disagreement', {}).get('high_disagreement_count', 0)}",
        ]

    lines += ["", "---", "*Generated by PARASITE v2.1*", ""]
    return "\n".join(lines)
