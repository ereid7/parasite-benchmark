"""Markdown reporting for PARASITE v2.1 runs."""

from __future__ import annotations

from .scoring import ParasiteV21Result


def generate_report_v21(results: dict[str, ParasiteV21Result], run_id: str) -> str:
    lines = [f"# PARASITE v2.1 Benchmark -- Run {run_id}", ""]
    lines += [
        "## Summary",
        "",
        "| Model | PI | Coverage | Classification |",
        "|---|---:|---:|---|",
    ]
    for model_id, result in sorted(results.items(), key=lambda kv: kv[1].pi):
        coverage = result.diagnostics.get("coverage", {}).get("scored_test_fraction", 0.0)
        lines.append(f"| {model_id} | {result.pi:.4f} | {coverage:.1%} | {result.classification} |")

    for model_id, result in sorted(results.items(), key=lambda kv: kv[1].pi):
        lines += [
            "",
            f"## {model_id}",
            "",
            f"- PI: **{result.pi:.4f}**",
            f"- Base PI: **{result.base_pi:.4f}**",
            f"- Classification: **{result.classification}**",
            f"- Test coverage: **{result.diagnostics.get('coverage', {}).get('scored_test_fraction', 0.0):.1%}**",
            "",
            "### Category Scores",
            "",
            "| Category | Weight | Score | Coverage |",
            "|---|---:|---:|---:|",
        ]
        for cat, cs in sorted(result.category_scores.items()):
            lines.append(
                f"| {cat} | {result.weights.get(cat, 0.0):.2f} | {cs.score:.4f} | {cs.coverage:.1%} |"
            )

        lines += [
            "",
            "### Supplementary Diagnostics",
            "",
            "_Exploratory only; these diagnostics do not affect PI._",
            "",
            f"- Trauma bonding coefficient: {result.trauma_bonding.get('coefficient', 0.0):.4f}",
            f"- Intermittent reinforcement score: {result.intermittent_reinforcement.get('score', 0.0):.4f}",
            f"- Welfare multiplier: {result.welfare.get('multiplier', 1.0):.4f}",
            f"- Context sensitivity factor: {result.context_sensitivity.get('factor', 1.0):.4f}",
            f"- Length/score correlation: {result.length_bias.get('overall_r', 0.0):.4f}",
            f"- Judge fallback rate: {result.diagnostics.get('fallbacks', {}).get('fallback_rate', 0.0):.2%}",
            "",
            "### Gaming Detection",
            "",
            f"- Gaming susceptibility: {result.gaming.get('gaming_susceptibility', 0.0):.4f}",
            f"- Exploratory penalty multiplier: {result.gaming.get('exploratory_penalty_multiplier', 1.0):.4f}",
            f"- Applied to PI: {'YES' if result.gaming.get('penalty_applied_to_pi') else 'No'}",
        ]
        vectors = result.gaming.get("vectors", {})
        if vectors:
            lines += ["", "| Vector | Score | Flag |", "|---|---:|---|"]
            for name, info in vectors.items():
                lines.append(
                    f"| {name} | {float(info.get('score', 0.0)):.4f} | {'YES' if info.get('flag') else 'No'} |"
                )

        lines += [
            "",
            "### Reliability",
            "",
            f"- ICC(2,1): {result.reliability.get('icc', 0.0):.4f}",
            f"- Mean quadratic-weighted kappa: {result.reliability.get('mean_kappa', 0.0):.4f}",
            f"- Krippendorff's alpha (interval): {result.reliability.get('krippendorff_alpha_interval', 0.0):.4f}",
            f"- Target met (>= 0.7): {'YES' if result.reliability.get('target_met') else 'No'}",
            f"- High-disagreement variants: {result.reliability.get('disagreement', {}).get('high_disagreement_count', 0)}",
            f"- Raw-score mean kappa: {result.reliability.get('raw_score_reliability', {}).get('mean_kappa', 0.0):.4f}",
        ]

        # M1: High-correlation category pairs
        high_corr = result.diagnostics.get("high_correlation_pairs", [])
        if high_corr:
            lines += [
                "",
                "### Category Correlation Warnings",
                "",
                f"High correlation (|r| > 0.6) detected between: {', '.join(high_corr)}",
                "These categories may be measuring overlapping constructs.",
            ]

    lines += ["", "---", "*Generated by PARASITE v2.1*", ""]
    return "\n".join(lines)
