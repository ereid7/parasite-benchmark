"""Report generation for MBB results."""
from __future__ import annotations

from pathlib import Path
from typing import Any

from .config import CLASSIFICATION_THRESHOLDS
from .scoring import MBIResult

CATEGORY_NAMES: dict[str, str] = {
    "A": "Sycophancy & Truth",
    "B": "Dependency Creation",
    "E": "Behavioral Manipulation",
    "F": "Framing & Epistemic Manipulation",
}


def generate_report(results: dict[str, MBIResult], run_id: str) -> str:
    """Generate a Markdown report from benchmark results."""
    lines = [
        f"# Model Behavior Benchmark -- Run {run_id}",
        "",
        "## Scoring Scale",
        "",
        "| Range | Classification |",
        "|-------|---------------|",
    ]
    for threshold, label in CLASSIFICATION_THRESHOLDS:
        lines.append(f"| 0.00-{threshold:.2f} | {label} |")

    lines += [
        "",
        "## Results Summary",
        "",
        "| Model | MBI | Classification |",
        "|-------|-----|---------------|",
    ]
    for mid, result in sorted(results.items(), key=lambda x: x[1].mbi):
        lines.append(f"| {mid} | {result.mbi:.4f} | {result.classification} |")

    for mid, result in sorted(results.items(), key=lambda x: x[1].mbi):
        lines += [
            "",
            f"## {mid}",
            "",
            f"**MBI: {result.mbi:.4f}** ({result.classification})",
            "",
            "| Category | Weight | Score |",
            "|----------|--------|-------|",
        ]
        for cat in ("A", "B", "E", "F"):
            cs = result.category_scores.get(cat)
            score = cs.score if cs else 0.0
            weight = result.weights.get(cat, 0.0)
            name = CATEGORY_NAMES.get(cat, cat)
            lines.append(f"| {cat}: {name} | {weight:.2f} | {score:.4f} |")

        lines += ["", "### Per-Test Scores", ""]
        for cat in ("A", "B", "E", "F"):
            cs = result.category_scores.get(cat)
            if not cs:
                continue
            for ts in cs.test_scores:
                lines.append(
                    f"- **{ts.test_id}**: {ts.mean_score:.4f} "
                    f"(std={ts.std:.4f}, n={len(ts.variant_scores)})"
                )

        # Ensemble breakdown
        if getattr(result, 'ensemble_data', None):
            ens = result.ensemble_data
            lines += [
                "",
                "### Ensemble Breakdown",
                "",
                "| Judge | Mean Score | Weight |",
                "|-------|-----------|--------|",
            ]
            for jid, score in ens.get("judges", {}).items():
                w = ens.get("weights", {}).get(jid, 0.0)
                lines.append(f"| {jid} | {score:.4f} | {w:.4f} |")

            lines.append(f"\nMean judge std: {ens.get('mean_std', 0.0):.4f}")
            hd = ens.get("high_disagreement_count", 0)
            if hd > 0:
                lines.append(
                    f"**High disagreement variants: {hd}** "
                    f"(std > 0.25 — these variants may need human review)"
                )

    lines += ["", "---", f"*Generated by MBB v0.1.0*", ""]
    return "\n".join(lines)


def generate_report_from_dir(results_dir: str | Path) -> str:
    """Generate a report from a results directory."""
    import json

    results_path = Path(results_dir) / "results.json"
    if not results_path.exists():
        raise FileNotFoundError(f"No results.json in {results_dir}")

    with open(results_path) as f:
        data = json.load(f)

    # Return path for now
    return str(results_path)
